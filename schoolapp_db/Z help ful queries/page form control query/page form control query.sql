SELECT
	P.ID,
	P.PAGE_CODE,
	PAGE_NAME,
	F.ID,
	F.FORM_CODE,
	F.FORM_NAME,
	C.ID,
	C.CONTROL_CODE,
	C.CONTROL_NAME
FROM
	PAGES P,
	FORMS F,
	FORM_CONTROLS FC,
	CONTROLS C
WHERE
	P.ID = PAGE_ID
	AND F.ID = FC.FORM_ID
	AND FC.CONTROL_ID = C.ID
	AND P.ID = 5
	AND FC.FORM_ID = 5
	AND C.ID = 1


	select *From form_status_master